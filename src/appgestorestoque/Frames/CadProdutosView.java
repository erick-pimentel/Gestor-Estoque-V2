    private void btn_produtosEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_produtosEditarActionPerformed
        try {
            String searchInput = JOptionPane.showInputDialog(this,
                    "Insira o codigo, nome ou ID do produto a editar:",
                    "Buscar Produto", JOptionPane.QUESTION_MESSAGE);
            if (searchInput == null || searchInput.trim().isEmpty()) {
                return;
            }

            Produto produto = PdService.buscarProduto(searchInput.trim());
            if (produto == null) {
                JOptionPane.showMessageDialog(this,
                        "Produto '" + searchInput + "' nao encontrado.",
                        "Produto nao encontrado", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String nomeProduto = JOptionPane.showInputDialog(this, "Nome do Produto:", produto.nomeProduto());
            if (nomeProduto == null) return;
            String codigoProduto = JOptionPane.showInputDialog(this, "Codigo do Produto:", produto.codigoProduto());
            if (codigoProduto == null) return;
            String quantidadeStr = JOptionPane.showInputDialog(this, "Quantidade em Estoque:", String.valueOf(produto.quantidadeEstoque()));
            if (quantidadeStr == null) return;
            String valorCompraStr = JOptionPane.showInputDialog(this, "Valor de Compra (ex: 10.50):", produto.valorCompra().toString());
            if (valorCompraStr == null) return;
            String valorVendaStr = JOptionPane.showInputDialog(this, "Valor de Venda (ex: 15.50):", produto.valorVenda().toString());
            if (valorVendaStr == null) return;
            String validadeStr = JOptionPane.showInputDialog(this, "Validade (yyyy-MM-dd):", produto.validadeProduto().toString());
            if (validadeStr == null) return;
            String idFornecedorStr = JOptionPane.showInputDialog(this, "ID do Fornecedor:", String.valueOf(produto.idFornecedor()));
            if (idFornecedorStr == null) return;

            PdService.editarProduto(
                    searchInput.trim(),
                    nomeProduto,
                    codigoProduto,
                    quantidadeStr,
                    valorCompraStr,
                    valorVendaStr,
                    validadeStr,
                    idFornecedorStr
            );

            JOptionPane.showMessageDialog(this,
                    "Produto atualizado com sucesso!",
                    "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            txt_produtosNome.setText("Insira o nome do produto");
            txt_produtosCodigo.setText("Insira o codigo do produto");
            txt_produtosCompra.setText("Insira o preco de compra");
            txt_produtosVenda.setText("Insira o preco de venda");
            combo_produtosFornecedor.setSelectedIndex(combo_produtosFornecedor.getItemCount() > 0 ? 0 : -1);
            txt_produtosValidade.setText("Insira o prazo de validade");
            txt_produtosQuantidade.setText("Insira a quantidade");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao editar: " + e.getMessage(), "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_produtosEditarActionPerformed

    private void btn_produtosExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_produtosExcluirActionPerformed
        try {
            String searchInput = JOptionPane.showInputDialog(this,
                    "Insira o código, nome ou ID do produto a deletar:",
                    "Buscar Produto", JOptionPane.QUESTION_MESSAGE);

            if (searchInput == null || searchInput.trim().isEmpty()) {
                return; // User cancelled
            }

            Produto produto = PdService.buscarProduto(searchInput.trim());

            if (produto == null) {
                JOptionPane.showMessageDialog(this,
                        "Produto '" + searchInput + "' não encontrado.",
                        "Produto não encontrado", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Confirm deletion
            int confirmacao = JOptionPane.showConfirmDialog(this,
                    "Tem certeza que deseja deletar o produto:\n" +
                    "Nome: " + produto.nomeProduto() + "\n" +
                    "Código: " + produto.codigoProduto() + "\n" +
                    "ID: " + produto.idProduto() + "\n\n" +
                    "Esta ação não pode ser desfeita!",
                    "Confirmar Exclusão", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (confirmacao != JOptionPane.YES_OPTION) {
                return; // User cancelled
            }

            PdService.excluirProdutoPorId(produto.idProduto());

            JOptionPane.showMessageDialog(this,
                    "Produto deletado com sucesso!",
                    "Exclusão", JOptionPane.INFORMATION_MESSAGE);

            // Clear fields
            txt_produtosNome.setText("Insira o nome do produto");
            txt_produtosCodigo.setText("Insira o código do produto");
            txt_produtosCompra.setText("Insira o preco de compra");
            txt_produtosVenda.setText("Insira o preco de venda");
            combo_produtosFornecedor.setSelectedIndex(combo_produtosFornecedor.getItemCount() > 0 ? 0 : -1);
            txt_produtosValidade.setText("Insira o prazo de validade");
            txt_produtosQuantidade.setText("Insira a quantidade");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao deletar: " + e.getMessage(), "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btn_produtosExcluirActionPerformed

