    private void btn_produtosCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_produtosCadastrarActionPerformed
        if(user==null){
            JOptionPane.showMessageDialog(this, "Erro na autenticacao de usuario!\nFaça login novamente.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            FornecedorComboItem fornecedorSelecionado = (FornecedorComboItem) combo_produtosFornecedor.getSelectedItem();
            if (fornecedorSelecionado == null || fornecedorSelecionado.id() <= 0) {
                JOptionPane.showMessageDialog(this, "Selecione um fornecedor valido.",
                        "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
            PdService.cadastrarProduto
                    (
                            user,
                            fornecedorSelecionado.id(),
                            txt_produtosNome.getText(),
                            txt_produtosCodigo.getText(),
                            txt_produtosQuantidade.getText(),
                            txt_produtosCompra.getText(),
                            txt_produtosVenda.getText(),
                            txt_produtosValidade.getText()
                    );
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!",
                    "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            //limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_produtosCadastrarActionPerformed

