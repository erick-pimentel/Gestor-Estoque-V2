    private void carregarFornecedoresNoCombo() {
        combo_produtosFornecedor.removeAllItems();
        List<Fornecedor> fornecedores = fornecedorService.listarTodos();
        for (Fornecedor fornecedor : fornecedores) {
            combo_produtosFornecedor.addItem(new FornecedorComboItem(
                    fornecedor.idFornecedor(),
                    fornecedor.nomeFornecedor()
            ));
        }
        if (combo_produtosFornecedor.getItemCount() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Nenhum fornecedor cadastrado. Cadastre um fornecedor antes de cadastrar produtos.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }

    private record FornecedorComboItem(int id, String nome) {
        @Override
        public String toString() {
            return nome;
        }
    }

