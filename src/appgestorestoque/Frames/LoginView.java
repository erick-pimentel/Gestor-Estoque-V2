    public LoginView(ILoginService loginService) {
        this.loginService = loginService;
        initComponents();
        this.setTitle("Login - Gestor Estoques");
        this.setResizable(false);


        txt_user.setText("Insira seu usuário");
        txt_user.setForeground(Color.BLACK);

        // Preserve LAF echo char (the masked char used by the current LAF)
        defaultEchoChar = txt_pass.getEchoChar();

        // Setup placeholder for password field: show plain text and disable the
        // visibility checkbox until the user types a real password.
        passPlaceholderActive = true;
        txt_pass.setText("Insira sua senha");
        txt_pass.setForeground(Color.BLACK);
        txt_pass.setEchoChar((char) 0); // show placeholder text
        turnVisibleBox.setSelected(false);
        turnVisibleBox.setEnabled(false);

        // Enable the visibility checkbox when the user types something that is
        // not the placeholder. Use a document listener so it reacts to typing/paste.
        txt_pass.getDocument().addDocumentListener(new DocumentListener() {
            private void update() {
                String current = new String(txt_pass.getPassword());
                boolean hasReal = !current.isEmpty() && !passPlaceholderActive;
                // If placeholder is active but user typed (some LAFs may keep it),
                // consider that typing cleared it via focusGained handler.
                turnVisibleBox.setEnabled(hasReal);
            }
            @Override public void insertUpdate(DocumentEvent e) { update(); }
            @Override public void removeUpdate(DocumentEvent e) { update(); }
            @Override public void changedUpdate(DocumentEvent e) { update(); }
        });
    }

