    private void txt_passFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_passFocusGained
        // If placeholder text is shown, clear it when the user focuses the field
        if (passPlaceholderActive) {
            passPlaceholderActive = false;
            txt_pass.setText("");
            txt_pass.setForeground(Color.BLACK);
            txt_pass.setEchoChar(defaultEchoChar);
            // After clearing placeholder, the document listener will enable the checkbox when the user types
            turnVisibleBox.setEnabled(false);
        }
    }//GEN-LAST:event_txt_passFocusGained

