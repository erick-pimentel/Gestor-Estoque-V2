    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        String user = txt_user.getText();
        String pass = new String(txt_pass.getPassword());

        if ("Insira seu usuário".equals(user)) {
            user = "";
        }
        // If placeholder is active, treat password as empty
        if (passPlaceholderActive) {
            pass = "";
        }

        if (user == null || user.trim().isEmpty() || pass.trim().isEmpty()) {
            msg_erro.setText("Preencha usuario e senha.");
            msg_erro.setForeground(Color.RED);
            return;
        }

        try {
            User usuario = loginService.autenticar(user.trim(), pass);
            if (usuario == null) {
                msg_erro.setText("Usuario ou senha incorretos!");
                msg_erro.setForeground(Color.RED);
                return;
            }

            msg_erro.setText("");
            MenuView menu = new MenuView();
            menu.setUser(usuario);
            menu.setVisible(true);
            this.dispose();
        } catch (IllegalStateException e) {
            msg_erro.setText("Usuario inativo!");
            msg_erro.setForeground(Color.RED);
        } catch (SQLException e) {
            msg_erro.setText("Erro no servidor!");
            msg_erro.setForeground(Color.RED);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

    private void turnVisibleBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_turnVisibleBoxActionPerformed
        if (turnVisibleBox.isSelected()) {
            txt_pass.setEchoChar((char) 0);
        } else {
            txt_pass.setEchoChar(defaultEchoChar);
        }
    }//GEN-LAST:event_turnVisibleBoxActionPerformed

