    private void initTabela() {
        tableModel = new DefaultTableModel(new Object[]{"Nome", "Perfil"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 1;
            }
        };
        tableGestao.setModel(tableModel);
        tableGestao.setRowHeight(32);

        sorter = new TableRowSorter<>(tableModel);
        tableGestao.setRowSorter(sorter);

        tableGestao.getColumnModel().getColumn(1).setCellRenderer(new PerfilButtonRenderer());
        tableGestao.getColumnModel().getColumn(1).setCellEditor(new PerfilButtonEditor(new JCheckBox()));
    }

    private void carregarUsuarios() {
        try {
            usuarios.clear();
            usuarios.addAll(userManagementService.listarUsuarios());

            tableModel.setRowCount(0);
            for (UserProfile item : usuarios) {
                tableModel.addRow(new Object[]{item.nome(), "VER PERFIL"});
            }

            aplicarFiltroPerfil();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void aplicarFiltroPerfil() {
        if (sorter == null) {
            return;
        }

        String filtro = (String) comboFiltro.getSelectedItem();
        if (filtro == null || "Todos".equalsIgnoreCase(filtro)) {
            sorter.setRowFilter(null);
            return;
        }

        sorter.setRowFilter(new RowFilter<>() {
            @Override
            public boolean include(Entry<? extends DefaultTableModel, ? extends Integer> entry) {
                int modelRow = entry.getIdentifier();
                if (modelRow < 0 || modelRow >= usuarios.size()) {
                    return false;
                }

                String perfil = usuarios.get(modelRow).perfil();
                return perfil != null && perfil.equalsIgnoreCase(filtro);
            }
        });
    }

    private void mostrarPerfilCompleto(int viewRow) {
        if (viewRow < 0) {
            return;
        }

        int modelRow = tableGestao.convertRowIndexToModel(viewRow);
        if (modelRow < 0 || modelRow >= usuarios.size()) {
            return;
        }

        UserDetailsDialog dialog = new UserDetailsDialog(this, usuarios.get(modelRow));
        dialog.setVisible(true);
    }

    class PerfilButtonRenderer extends JButton implements TableCellRenderer {
        public PerfilButtonRenderer() {
            setOpaque(true);
            setText("VER PERFIL");
        }

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            setText(value == null ? "VER PERFIL" : value.toString());
            return this;
        }
    }

    class PerfilButtonEditor extends DefaultCellEditor {
        private final JButton button;
        private int selectedRow = -1;
        private String label;

        public PerfilButtonEditor(JCheckBox checkBox) {
            super(checkBox);
            button = new JButton();
            button.setOpaque(true);
            button.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    fireEditingStopped();
                }
            });
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            selectedRow = row;
            label = value == null ? "VER PERFIL" : value.toString();
            button.setText(label);
            return button;
        }

        @Override
        public Object getCellEditorValue() {
            mostrarPerfilCompleto(selectedRow);
            return label;
        }
    }
