    private void btn_deleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteUserActionPerformed
        String criterio = JOptionPane.showInputDialog(this,
                "Informe o ID ou nome do usuario para excluir:",
                "Excluir Usuario",
                JOptionPane.QUESTION_MESSAGE);

        if (criterio == null || criterio.trim().isEmpty()) {
            return;
        }

        try {
            UserProfile alvo = userManagementService.buscarPorIdOuNome(criterio);
            if (alvo == null) {
                JOptionPane.showMessageDialog(this, "Usuario nao encontrado.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int confirmacao = JOptionPane.showConfirmDialog(this,
                    "Deseja excluir o usuario '" + alvo.nome() + "' (ID " + alvo.id() + ")?",
                    "Confirmar exclusao",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE);

            if (confirmacao != JOptionPane.YES_OPTION) {
                return;
            }

            userManagementService.excluirPorIdOuNome(criterio);
            carregarUsuarios();
            JOptionPane.showMessageDialog(this, "Usuario excluido com sucesso.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_deleteUserActionPerformed

    private void btn_userAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_userAddActionPerformed
        UserFormDialog dialog = new UserFormDialog(this, "Cadastrar Usuario", null);
        dialog.setVisible(true);
        if (!dialog.isConfirmado()) {
            return;
        }

        try {
            userManagementService.cadastrarUsuario(new UserRegistrationData(
                    dialog.getNome(),
                    dialog.getEmail(),
                    dialog.getSenha(),
                    dialog.getPerfil(),
                    dialog.isAtivo()
            ));
            carregarUsuarios();
            JOptionPane.showMessageDialog(this, "Usuario cadastrado com sucesso.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_userAddActionPerformed

    private void btn_userEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_userEditActionPerformed
        String criterio = JOptionPane.showInputDialog(this,
                "Informe o ID ou nome do usuario para editar:",
                "Editar Usuario",
                JOptionPane.QUESTION_MESSAGE);

        if (criterio == null || criterio.trim().isEmpty()) {
            return;
        }

        try {
            UserProfile alvo = userManagementService.buscarPorIdOuNome(criterio);
            if (alvo == null) {
                JOptionPane.showMessageDialog(this, "Usuario nao encontrado.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            UserFormDialog dialog = new UserFormDialog(this, "Editar Usuario", alvo);
            dialog.setVisible(true);
            if (!dialog.isConfirmado()) {
                return;
            }

            userManagementService.atualizarUsuario(new UserUpdateData(
                    alvo.id(),
                    dialog.getNome(),
                    dialog.getEmail(),
                    dialog.getSenha(),
                    dialog.getPerfil(),
                    dialog.isAtivo()
            ));
            carregarUsuarios();
            JOptionPane.showMessageDialog(this, "Usuario atualizado com sucesso.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_userEditActionPerformed

